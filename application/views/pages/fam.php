<h2 id="whatarefams">WHAT ARE FAMs</h2>

<p>
	It takes faith, time, money, and emotion to prioritize at-risk kids and their families. We’re grateful when a church partner decides to make this investment to care for vulnerable children, and we want to do all we can to help push forward their mission. To alleviate the stress and confusion that can come with creating any new ministry, we’ve created a <strong>step-by–step model</strong> called Family Advocacy Ministries (FAMs).
</p>

<h2 id="whyfams">WHY FAMs?</h2>

<p>
	Churches are often full of people who want to make a positive impact in the lives of vulnerable children, but don’t always know how. We believe if churches are empowered to openly and effectively share about the problems at-risk children are experiencing, families will step up, either by becoming a foster or adoptive family or by deciding to support a family in crisis. They simply need a plan.
</p>

<p>
	<strong>Promise686 implements FAMs to help bring about a positive impact in the lives of vulnerable children by:</strong>
</p>

<ul>
	<li>Recruiting &amp; Equipping families to care for at-risk kids</li>
	<li>Serving foster &amp; adoptive families, and biological families in crisis</li>
	<li>Advocating on behalf of families and vulnerable children</li>
	<li>Ministering to families and meeting physical, emotional, and spiritual needs</li>
</ul>

<p>
	<strong>Promise provides the training, resources, and guidance needed to empower churches to set up and maintain successful FAMs.</strong>
</p>

<p>
	<strong>START SMALL OR GO ALL OUT:</strong>
</p>

<p>
	We believe every church can do <em>something</em> to make a difference in the child welfare crisis. Depending on your church’s area of interests, resources available, and families in need, <strong>your</strong> something could look like:
</p>

<ul>
	<li>Praying diligently and consistently</li>
	<li>Raising awareness around the needs of vulnerable children</li>
	<li>Recruiting families to consider adoption and fostering</li>
	<li>Serving foster &amp; adoptive families, and biological families in crisis through Care Communities</li>
	<li>Meeting physical and financial needs</li>
	<li>Providing adoption grants &amp; resources to the families in your congregation</li>
	<li>Ministering to families and meeting emotional and spiritual needs</li>
</ul>

<p>
	Every FAM may look different but they all have one thing in common: needs are met and lives are changed. The list above shows a few ways to engage, but simply fill out the form to get started with <em>something</em>. Nothing is too small!
</p>


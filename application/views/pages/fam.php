<h2 id="learningmoreaboutfams%3A">LEARNING MORE ABOUT FAMs:</h2>

<p>
	<strong>CHURCH ADVOCATES + CLINICS</strong>
</p>

<p>Every Family Advocacy Ministry (FAM) needs leaders to create awareness, equip volunteers, and spark action. We call these leaders <strong>Church Advocates</strong>. All Advocates are trained at a one-day Clinic where they receive everything needed to create and maintain a successful FAM within their local church. In addition to the Clinic, Promise686 provides resources and ongoing support to Church Advocates and their volunteers. As an Advocate, we want you to be completely equipped to best serve foster &amp; adoptive families, and biological families in crisis.
</p>

<p>
	<strong><a href="/clinic">See our upcoming Clinic dates</a> and learn more about becoming a Church Advocate.</strong>
</p>

<p>
	<strong>WHO TO SERVE</strong>
</p>

<p>
	Not all FAMs will serve foster, adoptive, and biological families. Many times, FAMs decide to start out with one particular area of focus, like supporting foster families or raising awareness around adoption needs. Once FAMs are a little more established, we often see them expand their reach and the types of families they serve. The one thing all FAMs have in common is that they care for vulnerable children by supporting families in need.

</p>

<p>
	<strong>WHY PARTNER WITH PROMISE686</strong>
</p>

<p>
	Promise provides the training, resources, and guidance needed to empower church leaders, ministry leaders, and volunteers to set up and maintain successful FAMs. In fact, we’ve trained nearly 600 churches in 19 states and the impact made through these church partners is significant.
</p>

<p>
	Did you know the national average of families who quit fostering within the first year is about 50%? When supported by a FAM and its Care Communities, over 90% continue fostering into a second year. Care Communities are just one way Family Advocacy Ministries bring about positive change in the child welfare crisis.
</p>

